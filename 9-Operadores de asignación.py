a=0
a+=5 #Esto es lo mismo que: a=a+5 (suma en asignacion)
print(a)
a-=2 #Esto es lo mismo que: a=a-2 (resta en asignacion)
print(a)
a*=13 #Esto es lo mismo que: a=a*13 (multiplicacion en asignacion)
print(a)
a/=3 #Esto es lo mismo que: a=a/3 (division en asignacion)
print(a)
a**=3 #Esto es lo mismo que: a=a**3 (potencia en asignacion)
print(a)
a%=5 #Esto es lo mismo que: a=a%5 (modulo o resto en asignacion)
print(a)

